#include<bits/stdc++.h>
using namespace std;
int main()
{
    float n,m;
    cin>>n>>m;
    
    cout<<"floor "<<n<<" / "<<m<<" "<<"="<<" "<<floor(n/m)<<"\n";
    cout<<"ceil "<<n<<" / "<<m<<" "<<"="<<" "<<ceil(n/m)<<"\n";
    cout<<"round "<<n<<" / "<<m<<" "<<"="<<" "<<round(n/m)<<"\n";

    return 0;
}
